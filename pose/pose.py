	def PDB_entry(self, atom, n, a, l, r, c, s, i, x, y, z, o, t, q, e):
		''' Construct a PDB atom data row '''
		ATOM = '{:<6}'.format(atom)
		N = '{:>5} '.format(n)
		A = '{:<4}'.format(a)
		L = '{:<1}'.format(l)
		R = '{:>3}'.format(r)
		C = '{:>2}'.format(c)
		S = '{:>4}'.format(s)
		I = '{:>1}   '.format(i)
		X = '{:>8.3f}'.format(x)
		Y = '{:>8.3f}'.format(y)
		Z = '{:>8.3f} '.format(z)
		O = '{:>5.2f} '.format(o)
		T = ('{:>5.2f} '.format(t))[:6]
		Q = '{:>10}'.format('')
		E = '{:<2} \n'.format(e)
		entry = ATOM + N + A + L + R + C + S + I + X + Y + Z + O + T + Q + E
		return entry
	def CIF_entry(self, atom, n, a, l, r, c, label_seq, x, y, z, o, t, q, e):
		''' Construct a mmCIF _atom_site data row '''
		alt = '.' if l == '' else l
		charge = '?' if q == 0.0 else '{:.3f}'.format(q)
		fields = [
			atom, str(n), e, a, alt, r, c, '1',
			str(label_seq), '?',
			'{:.3f}'.format(x), '{:.3f}'.format(y),
			'{:.3f}'.format(z),
			'{:.2f}'.format(o), '{:.2f}'.format(t),
			charge,
			str(label_seq), r, c, a, '1']
		return ' '.join(fields) + '\n'
	def atom_residue_iter(self):
		''' Yield (atom_item, coordinate, AAindex) for every atom '''
		AAs    = self.data['Amino Acids']
		AAidx  = 0
		length = len(AAs[AAidx][2]) + len(AAs[AAidx][3]) - 1
		for atom, coord in zip(
				self.data['Atoms'].items(),
				self.data['Coordinates']):
			yield atom, coord, AAidx
			if length:
				length -= 1
			else:
				AAidx += 1
				try:
					length = (len(AAs[AAidx][2]) + len(AAs[AAidx][3]) - 1)
				except (KeyError, IndexError):
					pass
